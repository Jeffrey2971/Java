package Day01.chapter01;

/*
    计算器类
 */

public class t_Calculator_1 {
    /**
     * 加法
     * @param a
     * @param b
     * @return
     */
    public int add(int a ,int b){
        return a+b;
    }

    /**
     * 减法
     * @param a
     * @param b
     * @return
     */
    public int sub(int a ,int b){
        return a-b;
    }

}
